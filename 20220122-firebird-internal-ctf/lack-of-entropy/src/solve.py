l, r = 0, 2**256

while l + 1 < r:
    pm = (l + r) // 2
    qm = int(gmpy2.digits(pm, 3))

    if pm * qm <= n: l = pm
    else:            r = pm

p = l
q = n // p

assert p * q == n
assert gmpy2.is_prime(p) and gmpy2.is_prime(q)

